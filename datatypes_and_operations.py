# Introduction

print(3 + 2)   # addition(+)
print(3 - 2)   # subtraction(-)
print(3 * 2)   # multiplication(*)
print(3 / 2)   # division(/)
print(3 ** 2)  # exponential(**)
print(3 % 2)   # modulus(%) (The residue of a division)
print(3 // 2)  # Floor division operator(//) (It is a division but it rounds the result to the nearest number)

# Checking data types

print(type(10))                  # Int (integer numbers)
print(type(3.14))                # Float (numbers with decimals, not integers)
print(type(1 + 3j))              # Complex (imaginary numbers)
print(type('Anilu'))             # String (text)
print(type([1, 2, 3]))           # List (several numbers or strings)
print(type({'name':'Anilu'})) # Dictionary (like json files)
print(type({9.8, 3.14, 2.7}))    # Set      similar to a list
print(type((9.8, 3.14, 2.7)))    # Tuple    
print(type(3 == 3))              # Bool     true or false
print(type(3 >= 3))              # Bool     true or false